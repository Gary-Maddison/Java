// This is a "Class" — it's the container for all your code.
// The name (Main) must match your filename (Main.java) exactly.
public class Main {

    // This is the "Main Method" — it is the starting point of your program.
    // When you click 'Run', the computer looks specifically for this line to begin.
    public static void main(String[] args) {

        // This line tells the computer to output text to the console (terminal).
        // 'System' is a built-in Java class, 'out' is the output stream, and 'println'
        // means 'print line'.
        System.out.println("Hello World!");

    } // This curly brace closes the main method.
} // This curly brace closes the Main class.
